#include <stdio.h>
#include <string.h>

void demo(int dec, const char *flags) {
  char format[64] = "|%";
  strcat(strcat(format, flags), "i|\n");
  printf(format, dec);
}

int main() {
  demo(10, "5");  // |   10|
  demo(10, "-5"); // |10   |
  demo(10, "+5"); // |  +10|
  demo(10, "05"); // |00010|
  printf("\n");
  demo(12345, "3");   // |12345|
  demo(12345, "-3");  // |12345|
  demo(12345, "+3");  // |+12345|
  demo(12345, "003"); // |12345|
  printf("\n");
  demo(-10, "-3");  // |-10|
  demo(-10, "03");  // |-10|
  demo(-10, "04");  // |-010|
  demo(-12, "0-4"); // |-12 |
  demo(-12, "-04"); // |-12 |
  demo(10, "+-4");  // |+10 |
  demo(10, "-+4");  // |+10 |
  demo(11, "+0-4"); // |+11 |
  demo(11, "0+4");  // |+011|

  demo(26, "+05");  // |+0026|
  demo(-26, "+05"); // |+0026|

  demo(-26, "-05"); // |-26  |

  demo(26, "+-7");  // |+26    |
  demo(-26, "-+7"); // |-26    |

  demo(6, "-5"); // |6    |

  demo(5, " 5");     // |    5|
  demo(5, "- 5");    // | 5   |
  demo(5, "0 5");    // | 0005|
  demo(5, "0+ 5");   // |+0005|
  demo(5, "0+ 5");   // |+0005|
  demo(5, "0- 5");   // | 5   |
  demo(213, " ");    // | 213|
  demo(210, "- -5"); // | 210 |
  demo(-0, "+2");    // |+0|
  demo(+0, "+2");    // |+0|
}